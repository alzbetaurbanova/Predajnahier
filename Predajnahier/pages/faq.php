<h2>Ako to tu funguje? </h2>
Hry na tejto stránke sú z druhej ruky, teda niesú čisto nové. Od toho sa samozrejme odvíja i cena - nepoužívané, 
či málo opotrebované sa dosť približujú k trhovej cene, viac použité kúpite za malý peniaz. Avšak to neznamená 
že niesú funkčné. Garantujeme funkčnost hry.<br><br>

