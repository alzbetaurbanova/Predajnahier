Jednoducho! Stačí že klikneš na hru ktorú si chceš kúpiť, klikneš na tlačítko "Kúpiť" a vyplníš formulár.
Alebo nám napíšeš e-mail na adresu <a href="mailto:kupim@gamesky.sk?Objednavka" target="_top">kupim@gamesky.sk</a>.
E-mail by mal obsahovať meno, adresu, kontaktné údaje, spôsob platby a spôsob dopravy. Do 24 hodín ti príde na mail
odpoveď.<br><br>

